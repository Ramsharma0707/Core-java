package in.com.rays.Practice;

public class S_case_pract {
	public static void main(String[] args) {
	int n1=50;
	int n2=40;
	double result;
    char operation= '+';
    switch (operation ) {
    case '+':
    result=(n1+n2);
    System.out.println(""+result);
    break;
    case '-':
    	result=(n1-n2);
    	System.out.println(result);
    	break;
    }
	}
	

}
