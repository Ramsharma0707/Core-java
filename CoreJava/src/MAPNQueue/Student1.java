package MAPNQueue;

public class Student1 {
 int id ;
 String name;
 String Location;
public Student1(int id, String name, String location) {
	super();
	this.id = id;
	this.name = name;
	Location = location;
}
@Override
public String toString() {
	return "Student1 [id=" + id + ", name=" + name + ", Location=" + Location + "]";
}
 
}
